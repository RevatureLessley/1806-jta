CREATE TABLE CUSTOMER_ADDRESS(
    ADRESS_ID NUMBER(6)PRIMARY KEY,  
	STREET_NUMBER NUMBER(6), 
	STREET_NAME VARCHAR2(50) NOT NULL 
 );
CREATE TABLE CUSTOMER (
    CUSTOMER_ID NUMBER(6,0)PRIMARY KEY, 
	CUSTOMER_LNAME VARCHAR2(20)NOT NULL, 
	CUSTOMER_FNAME VARCHAR2(20)NOT NULL, 
	CUSTOMER_PASSWORD VARCHAR2(20) NOT NULL, 
	ADRESS_ID NUMBER(6), 
    CONSTRAINT FK_ADDRESS FOREIGN KEY (ADRESS_ID)
	  REFERENCES CUSTOMER_ADDRESS (ADRESS_ID )
);


  
CREATE TABLE BANK_BRANCH(
    BRANCH_ID NUMBER(6)PRIMARY KEY, 
	BRANCH_NAME VARCHAR2(30) NOT NULL , 
	BRANCH_CITY VARCHAR2(20) NOT NULL  
);

CREATE TABLE ACCOUNTS(
    ACCOUNT_TYPE_ID NUMBER(6)PRIMARY KEY,
	ACCOUNT_NAME VARCHAR2(25) NOT NULL, 
	ACCOUNT_INTREST_RATE NUMBER(6)
);

CREATE TABLE TRANSATIONS(
    TRAN_ID NUMBER(10)PRIMARY KEY,
    DEPOSITED NUMBER(10),
    WITHDRAW NUMBER(10)  
);
  
CREATE TABLE BANK_ACCOUNT(	
    ACCOUNT_ID NUMBER(6)PRIMARY KEY , 
	BRANCH_ID NUMBER(6), 
	ACCOUNT_TYPE_ID NUMBER(6), 
	ACCOUNT_BALANCE NUMBER(38),
    TRAN_ID NUMBER(10),
    CONSTRAINT FK_BRANCH_NAME FOREIGN KEY (BRANCH_ID)
	  REFERENCES BANK_BRANCH (BRANCH_ID), 
    CONSTRAINT FK_ACCOUNTS FOREIGN KEY (ACCOUNT_TYPE_ID)
	  REFERENCES ACCOUNTS (ACCOUNT_TYPE_ID),
       CONSTRAINT FK_TRAN_ID FOREIGN KEY (TRAN_ID)
	  REFERENCES TRANSATIONS (TRAN_ID)
) ;



CREATE TABLE ACCOUNT_2_CUSTOMER(
    CUSTOMER_ID NUMBER (6),
    ACCOUNT_ID NUMBER(6),
    CONSTRAINT FK_ACCOUNT_2_CUSTOMER FOREIGN KEY (CUSTOMER_ID)
	  REFERENCES CUSTOMER (CUSTOMER_ID ),
    CONSTRAINT FK_ACCOUNT_2_CUTOMER FOREIGN KEY (ACCOUNT_ID)
	  REFERENCES BANK_ACCOUNT (ACCOUNT_ID )
);




 
